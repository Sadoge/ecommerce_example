class Product {
  final String name;
  final double price;
  final bool hasDiscount;
  final double oldPrice;
  final String category;
  final String imagePath;
  final bool isFavourited;
  final String description;

  Product({
    this.name,
    this.price,
    this.hasDiscount,
    this.oldPrice,
    this.category,
    this.imagePath,
    this.isFavourited,
    this.description,
  });
}
